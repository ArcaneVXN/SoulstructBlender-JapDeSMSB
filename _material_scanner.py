def scan_chr():
    mtd_and_texture_types = []

    for chrbnd_path in VANILLA_CHR_PATH.glob("*.chrbnd.dcx"):
        if re.match(r"c[01]000", chrbnd_path.name):
            continue
        chrbnd = Binder(chrbnd_path)
        print(f"Reading FLVER in {chrbnd_path.name}...")
        flver = FLVER(chrbnd[200])
        for material in flver.materials:
            texture_types = [tex.texture_type for tex in material.textures]
            mtd_and_texture_types.append((Path(material.mtd_path).stem, texture_types))

    for mtd_texture_types in mtd_and_texture_types:
        print(mtd_texture_types)


def scan_map():

    with Path("map_mtd_textures.py").open("w") as f:
        f.write("MAP_MTD_DICT = {\n")
        for flver_path in VANILLA_MAP_PATH.rglob("*.flver.dcx"):
            print(f"Reading FLVER {flver_path.name}...")
            flver = FLVER(flver_path)
            for material in flver.materials:
                texture_types = [tex.texture_type for tex in material.textures]
                f.write(f"    ('{Path(material.mtd_path).stem}', {texture_types}),\n")
        f.write("}\n")


def get_unique_mtds():
    """
    FINDINGS:
        - In rare cases, materials seem to have textures they don't need. I assume these are just ignored.
        - I'll take the minimal set as canonical.
        - Almost every material has a "g_DetailBumpmap" texture, but not all:
            - `Ps_Wander_Ghost` has Diffuse, Bumpmap, Diffuse_2.
            - `M_4Stone[DSB]` has Diffse, Specular, Bumpmap.
            - `A12_NewWater` has Bumpmap.
        - [DSB] shaders require Diffuse, Specular, and/or Bumpmap textures, respectively.
        - [M] shaders have second '_2' copies of each texture (one [DB] case which still has two Specular textures).
    """
    mtd_dict = {}
    for mtd_name, texture_types in CHR_MTD_TEXTURES:
        if mtd_name not in mtd_dict:
            mtd_dict[mtd_name] = texture_types
        elif texture_types != mtd_dict[mtd_name]:
            print(f"# DIFFERENCE for {mtd_name}: {mtd_dict[mtd_name]} vs. {texture_types}")
            if len(texture_types) < len(mtd_dict[mtd_name]):
                mtd_dict[mtd_name] = texture_types  # update with shorter list

    print("MTD_TEXTURES_DICT = {")
    for mtd_name, texture_types in mtd_dict.items():
        print(f"    '{mtd_name}': {texture_types},")
    print("}")


